package model;

/**
 *
 * @author firem
 */
public class ContractDetail {
    private int id;
    private double price;

    public ContractDetail() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }
    
    public long getPriceLong(){
        return (long)price;
    }
}
